end


local function HandleDebuff()
    for i=1,40 do
        local name, _, _, _, _, _, _, _, _, spellId = UnitDebuff("player", i)
        if name then
            if spellId and AllSpellIDs[spellId] then
                if AllSpellIDs[spellId] == "Root" then
                    print(" ")
                    print("  You  are  rooted  !  (" .. name .. ")")
                    print(" ")
                elseif AllSpellIDs[spellId] == "Snare" then
                    print(" ")
                    print("  You  are  snared  !  (" .. name .. ")")
                    print(" ")
				elseif AllSpellIDs[spellId] == "Debuff" then
					print(" ")
					print("  You  are  rooted  !  (" .. name .. ")")
					print(" ")
                end
            elseif spellId then
				if name ~= "Dazed" then
                	print(" ")
					print('--------------')
                	print("Debuffed with:  " .. name .. "  ( ID: " .. spellId .. " )")
                	print("If it can be shapeshifted out of, it needs to be added to the AllSpellIDs table.")
					print('--------------')
                	print(" ")
				end
            end
        else
            break
        end
    end
end
local RootWatcher = CreateFrame("Frame")
RootWatcher:RegisterUnitEvent("UNIT_AURA", "player")
RootWatcher:SetScript("OnEvent", HandleDebuff)


